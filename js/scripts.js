// Call the init function on page load or when needed
function togglePricingDuration() {
  const durationParent = document.querySelector(".plans-pricing-nd .frame-15");
  if (!durationParent) return;

  const monthParentDiv = durationParent.children[0];
  const yearParentDiv = durationParent.children[1];

  const monthText = document.querySelector("#monthly");
  const yearText = document.querySelector("#yearly");

  if (!monthText || !yearText) return;

  const updateStyles = (el, removeClass, addClass) => {
    el.classList.remove(removeClass);
    el.classList.add(addClass);
  }

  // Add event listeners for 'Monthly' and 'Yearly' clicks
  monthText.addEventListener("click", () => {
    // Set style to Monthly button
    updateStyles(monthParentDiv, 'frame-16', 'your-session-has-tim-wrapper')
    updateStyles(monthText, 'default-duration-toggle', 'your-session-has-tim')
    // Remove style from Yearly button
    updateStyles(yearParentDiv, 'your-session-has-tim-wrapper', 'frame-16')
    updateStyles(yearText, 'your-session-has-tim', 'default-duration-toggle')

    updatePlans("monthly");
  });

  yearText.addEventListener("click", () => {
    // Set style to Yearly button
    updateStyles(yearParentDiv, 'frame-16', 'your-session-has-tim-wrapper')
    updateStyles(yearText, 'default-duration-toggle', 'your-session-has-tim')
    // Remove style from Monthly button
    updateStyles(monthParentDiv, 'your-session-has-tim-wrapper', 'frame-16')
    updateStyles(monthText, 'your-session-has-tim', 'default-duration-toggle')

    updatePlans("yearly");
  });
}

// Function to update the plans based on selected pricing duration
function updatePlans(duration) {
  const plans = document.querySelectorAll(".plans-pricing-3, .plans-pricing-4, .plans-pricing-5");

  // Update the text for each pricing card based on monthly or yearly
  plans.forEach(plan => {
    // Corrected to select the element with the class "text-wrapper-29"
    const priceMonth = plan.querySelector(".text-wrapper-29");

    if (!priceMonth) return;

    // Update the text based on the duration
    if (duration === "monthly") {
      priceMonth.textContent = "Month";  // Change the text content to "Month"
      priceMonth.setAttribute("data-i18n", "month");
    } else {
      priceMonth.textContent = "Year";  // Change the text content to "Year"
      priceMonth.setAttribute("data-i18n", "year");
    }
  });
  applyTranslations(currentLanguage);
}


function showSliderImage(index) {
  const nextArrow = document.getElementById('slider-next');
  const prevArrow = document.getElementById('slider-prev');

  // Handle arrow colors correctly
  if (index === 0) {
    // At the first image
    prevArrow.style.background = '#ffffff';
    nextArrow.style.background = '#0071ce';
  } else if (index === images.length - 1) {
    // At the last image
    nextArrow.style.background = '#ffffff';
    prevArrow.style.background = '#0071ce';
  } else {
    // In between
    nextArrow.style.background = '#0071ce';
    prevArrow.style.background = '#0071ce';
  }

  // Update the displayed image and active dot
  images.forEach((img, i) => {
    img.style.display = i === index ? 'block' : 'none';
    if (ellipses[i]) {
      ellipses[i].style.background = i === index ? '#0071ce' : '#ccc';
    }
  });
}

